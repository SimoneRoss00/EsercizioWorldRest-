package it.objectmethod.WorldCountry.model;

public class Country {

	private String codeCountry;
	private String nameCountry;
	private String continent;
	private String region;
	private Integer surfaceArea;
	private Integer indepYear;
	private Integer populationCountry;
	private Double lifeExpecnancy;
	private Double GNP;
	private Double GNPOld;
	private String localName;
	private String governmentForm;
	private String headOfState;
	

	public String getCodeCountry() {
		return codeCountry;
	}

	public void setCodeCountry(String codeCountry) {
		this.codeCountry = codeCountry;
	}

	public String getNameCountry() {
		return nameCountry;
	}

	public void setNameCountry(String nameCountry) {
		this.nameCountry = nameCountry;
	}

	public String getContinent() {
		return continent;
	}

	public void setContinent(String continent) {
		this.continent = continent;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public Integer getSurfaceArea() {
		return surfaceArea;
	}

	public void setSurfaceArea(Integer surfaceArea) {
		this.surfaceArea = surfaceArea;
	}

	public Integer getIndepYear() {
		return indepYear;
	}

	public void setIndepYear(Integer indepYear) {
		this.indepYear = indepYear;
	}

	public Integer getPopulationCountry() {
		return populationCountry;
	}

	public void setPopulationCountry(Integer populationCountry) {
		this.populationCountry = populationCountry;
	}

	public Double getLifeExpecnancy() {
		return lifeExpecnancy;
	}

	public void setLifeExpectancy(Double lifeExpecnancy) {
		this.lifeExpecnancy = lifeExpecnancy;
	}

	public Double getGNP() {
		return GNP;
	}

	public void setGNP(Double gNP) {
		GNP = gNP;
	}

	public Double getGNPOld() {
		return GNPOld;
	}

	public void setGNPOld(Double gNPOld) {
		GNPOld = gNPOld;
	}

	public String getLocalName() {
		return localName;
	}

	public void setLocalName(String localName) {
		this.localName = localName;
	}

	public String getGovernmentForm() {
		return governmentForm;
	}

	public void setGovernmentForm(String governmentForm) {
		this.governmentForm = governmentForm;
	}

	public String getHeadOfState() {
		return headOfState;
	}

	public void setHeadOfState(String headOfState) {
		this.headOfState = headOfState;
	}



}
